<script>
  const IMG_PATH = 'https://image.tmdb.org/t/p/w1280'

  export let movie

  function getClassByRate(vote) {
    if (vote >= 8) {
      return 'green'
    } else if (vote >= 5) {
      return 'orange'
    } else {
      return 'red'
    }
  }
</script>

<style>
  .movie {
    width: 300px;
    margin: 1rem;
    background-color: var(--secondary-color);
    box-shadow: 0 5px 5px rgba(0,0,0,0.2);
    position: relative;
    overflow: hidden;
    border-radius: 3px;
  }

  .movie img {
    width: 100%;
  }

  .movie-info {
    color: #eee;
    display: flex;
    align-items: center;
    justify-content: space-between;
    padding: 0.5rem 1rem 1rem;
    letter-spacing: .5px;
  }

  .movie-info h3 {
    margin-top: 0;
  }

  .movie-info span {
    background-color: var(--primary-color);
    padding: .25rem .5rem;
    border-radius: 3px;
    font-weight: bold;
  }

  .movie-info span.green {
    color: lightgreen;
  }

  .movie-info span.orange {
    color: orange;
  }

  .movie-info span.red {
    color: red;
  }

  .overview {
    background-color: #fff;
    padding: 2rem;
    position: absolute;
    left: 0;
    bottom: 0;
    right: 0;
    max-height: 100%;
    transform: translateY(101%);
    transition: transform 0.3s ease-in;
  }

  .movie:hover .overview {
    transform: translateY(0);
  }
</style>

<div class="movie">
  <img src={IMG_PATH + movie.poster_path} alt="{movie.title}">
  <div class="movie-info">
    <h3>{movie.title}</h3>
    <span class={getClassByRate(movie.vote_average)}>{movie.vote_average}</span>
  </div>
  <div class="overview">
    <h3>Overview</h3>
    {movie.overview}
  </div>
</div>
